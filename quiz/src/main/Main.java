package main;

public class Main {
	
//	# Class 
//		Adalah sebuah kerangka objek dalam Java. Karena Java merupakan Object-Oriented Language, 
//		maka dibutuhkan kerangka yang akan menjadi pondasi untuk objek yang akan dibuat.
		
//	# Object 
//		Adalah sebuah realisasi dari class. 
	
//	# Constructor 
//		Adalah sebuah method dasar yang akan membentuk sebuah objek. Constructor dapat berjumlah
//		lebih dari satu dengan catatan memiliki parameter yang berbeda-beda(shadowing)
	
//	# Inheritance 
//		Adalah sebuah konsep OOP dimana sebuah class meng-inherit atau mewarisi seluruh
//		atribut yang dimiliki oleh class lain. Hubungan antara kedua class ini adalah 
//		superclass dan subclass
	
//	# Encapsulation
//		Adalah sebuah konsep OOP dimana terdapat sebuah method yang "membungkus" parameter dalam 
//		sebuah class yang bersifat `private` agar dapat diakses oleh class lain dan/atau
//		dari class lain
		
//	 # Java Keyword (Access Modifiers)
//	 	Adalah sebuah parameter atau modifier yang dapat digunakan oleh atribut atau method 
//	 	untuk menentukan visibility dari method tersebut dalam suatu project. Terdapat 4 macam: 
//	 		- Public -> dapat diakses dari manapun dalam sebuah project. 
//	 		- Default -> dapat diakses dari dalam package yang sama
//	 		- Private -> dapat diakses hanya dari class itu sendiri
//	 		- Portected -> dapat diakses oleh class itu dan subclass nya
}
